#pragma region Библиотеки

#include <stdio.h>   // для функций ввода/вывода: printf, scanf, fgets
#include <string.h>  // для работы со строками: strlen, strcspn
#include <ctype.h>   // для работы с символами (регистром)
#include <locale.h>  // для установки локали (русский язык)

#pragma endregion

#pragma region Контроль памяти

#define ALPHABET_SIZE 26    // количество букв в латинском алфавите
#define MAX_STRING 1000     // максимальная длина текста для шифрования
#define MAX_WORD 100        // максимальная длина кодового слова

#pragma endregion

#pragma region Инициализация глобальных переменных

// Глобальные переменные

char cipher_alphabet[ALPHABET_SIZE + 1];  // алфавит замены 
char reverse_alphabet[ALPHABET_SIZE + 1]; // обратный алфавит (+1 нужен для хранения завершающего нуля '\0')

#pragma endregion


#pragma region Создание алфавита

// Функция создания алфавита замены на основе кодового слова

void make_alphabet(char *keyword) { // Вход: keyword - указатель на строку с кодовым словом
    int used[26] = {0};  // отмечаем, какие буквы уже использованы; used[0] = 1 означает, что буква 'A' уже есть в ключе
    int idx = 0;         // текущий индекс для заполнения алфавита замены
    
    // Переводим всё кодовое слово в верхний регистр
    for (int i = 0; keyword[i]; i++) {
        keyword[i] = toupper(keyword[i]);
    }
    
    // Проходим по буквам кодового слова и добавляем их в алфавит замены (без повторений)
    for (int i = 0; keyword[i]; i++) {
        int pos = keyword[i] - 'A';  // вычисляем позицию буквы (A=0, B=1, ..., Z=25)
        
        // проверяем, что позиция в пределах алфавита и буква ещё не использована
        if (pos >= 0 && pos < 26 && !used[pos]) {
            used[pos] = 1;               // помечаем букву как использованную
            cipher_alphabet[idx++] = keyword[i]; // добавляем букву в алфавит замены
        }
    }
    
    // Добавляем все оставшиеся буквы латинского алфавита
    // в естественном порядке (от A до Z)
    for (char c = 'A'; c <= 'Z'; c++) {
        int pos = c - 'A';      // позиция текущей буквы
        if (!used[pos]) {        // если буква ещё не была добавлена
            cipher_alphabet[idx++] = c;  // добавляем её
        }
    }
    
    // Добавляем завершающий нулевой символ в конец строки
    cipher_alphabet[idx] = '\0';
    
    // Создаём обратный алфавит для расшифровки подменой буковок
    for (int i = 0; i < 26; i++) {
        // для каждой буквы алфавита замены определяем её позицию
        int pos = cipher_alphabet[i] - 'A';
        // в обратном алфавите на этой позиции сохраняем исходную букву
        reverse_alphabet[pos] = 'A' + i;
    }
    reverse_alphabet[26] = '\0';  // завершающий ноль
}

#pragma endregion


#pragma region Шифрование 

// Функция шифрования текста
// text - исходный текст, result - буфер для результата


void encrypt(char *text, char *result) {
    // проходим по всем символам исходного текста
    for (int i = 0; text[i]; i++) {
        // проверяем, является ли символ буквой
        if (isalpha(text[i])) {
            // запоминаем, был ли символ в верхнем регистре
            int upper = isupper(text[i]);
            
            // переводим букву в верхний регистр для поиска в алфавите
            char c = toupper(text[i]);
            
            // находим замену: из алфавита замены берём букву с той же позицией
            char new_c = cipher_alphabet[c - 'A'];
            
            // сохраняем исходный регистр:
            // если исходная буква была заглавной - оставляем заглавной
            // если строчной - переводим замену в нижний регистр
            result[i] = upper ? new_c : tolower(new_c);
        } else {
            // небуквенные символы (пробелы, знаки препинания, цифры)
            // остаются без изменений
            result[i] = text[i];
        }
    }
    // добавляем завершающий ноль в конец строки результата
    result[strlen(text)] = '\0';
}

#pragma endregion

// Функция расшифрования текста
// text - зашифрованный текст, result - буфер для результата

#pragma region Расшифрование 

void decrypt(char *text, char *result) {
    // проходим по всем символам зашифрованного текста
    for (int i = 0; text[i]; i++) {
        // проверяем, является ли символ буквой
        if (isalpha(text[i])) {
            // запоминаем регистр символа
            int upper = isupper(text[i]);
            
            // переводим букву в верхний регистр
            char c = toupper(text[i]);
            
            // находим исходную букву через обратный алфавит
            char new_c = reverse_alphabet[c - 'A'];
            
            // восстанавливаем исходный регистр
            result[i] = upper ? new_c : tolower(new_c);
        } else {
            // небуквенные символы не изменяем
            result[i] = text[i];
        }
    }
    // добавляем завершающий ноль
    result[strlen(text)] = '\0';
}

#pragma endregion

// Главная функция программы 

int main() {

    #pragma region Главная функция
    // Устанавливаем русскую локаль для корректного отображения кириллицы
    setlocale(LC_ALL, "ru_RU.UTF-8");
    
    // Объявление переменных
    char keyword[MAX_WORD];   // для хранения кодового слова
    char input[MAX_STRING];   // для ввода текста пользователем
    char output[MAX_STRING];  // для результата шифрования/расшифрования
    int choice;               // выбор пользователя в меню
    
    // Вывод приветствия и шапки программы

    printf("Программа шифрования с кодовым словом\n");
    
    // Ввод кодового слова от пользователя
    printf("Введите кодовое слово (буквы не должны повторяться): ");
    fgets(keyword, MAX_WORD, stdin);     // читаем строку (включая пробелы)
    keyword[strcspn(keyword, "\n")] = '\0'; // удаляем символ перевода строки
    
    // Создаём алфавит замены на основе введённого кодового слова
    make_alphabet(keyword);
    
    // Выводим для наглядности исходный алфавит и полученный алфавит замены
    printf("\nИсходный алфавит: ");
    for (char c = 'A'; c <= 'Z'; c++) {
        printf("%c ", c);   // печатаем буквы от A до Z
    }
    
    printf("\nАлфавит замены:  ");
    for (int i = 0; i < 26; i++) {
        printf("%c ", cipher_alphabet[i]);  // печатаем созданный алфавит
    }
    printf("\n\n");
    

    // Главный цикл программы (меню)

    do {
        // Выводим меню выбора действия
        printf("1. Зашифровать\n");
        printf("2. Расшифровать\n");
        printf("3. Выйти\n");
        
        // Считываем выбор пользователя
        scanf("%d", &choice);
        getchar();  // очищаем буфер ввода от символа перевода строки
        
        // Обработка выбора пользователя
        if (choice == 1) {
            // Режим шифрования
            printf("Введите текст для шифрования: ");
            fgets(input, MAX_STRING, stdin);          // читаем текст
            input[strcspn(input, "\n")] = '\0';       // удаляем \n
            
            encrypt(input, output);                    // шифруем
            
            printf("Зашифрованный текст: %s\n", output); // выводим результат
        }
        else if (choice == 2) {
            // Режим расшифрования
            printf("Введите текст для расшифрования: ");
            fgets(input, MAX_STRING, stdin);          // читаем текст
            input[strcspn(input, "\n")] = '\0';
            
            decrypt(input, output);                    // расшифровываем
            
            printf("Расшифрованный текст: %s\n", output); // выводим результат
        }
        else if (choice == 3) {
            // Выход из программы
            printf("\nПрограмма завершена.\n");
        }
        else {
            // Обработка неверного ввода
            printf("\nНеверный выбор! Попробуйте снова.\n");
        }
        
    } while (choice != 3);  // повторяем, пока пользователь не выберет "Выйти"
    
    return 0;  // успешное завершение программы

    #pragma endregion

}
